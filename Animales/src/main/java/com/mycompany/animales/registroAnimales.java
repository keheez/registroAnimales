    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        
        String campo1 = nombre.getText(); // Se crea variables y se iguala a lo que est√° escrito en los campos de registro
        String campo2 = edad.getText();
        String campo3 = raza.getText();
        String campo4 = tamano.getText();
        String campo5 = dueno.getText();

        // Verificar si todos los campos estan completos
        if (campo1.isEmpty() || campo2.isEmpty() || campo3.isEmpty() || campo4.isEmpty() || campo5.isEmpty()) {
            // Si algun campo esta vacio, muestra un mensaje de error y no guarda los datos
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
            return;
        }

        // Lo que se extrajo de los campos de entrada se guarda en una matriz que esta declarado arriba del todo en el public
        Animal[animalindex][0] = campo1;
        Animal[animalindex][1] = campo2;
        Animal[animalindex][2] = campo3;
        Animal[animalindex][3] = campo4;
        Animal[animalindex][4] = campo5;

        animalindex++;
        // Una vez extraido todos los campos de entrada y guardado en una matriz, se limpian los campos de registro
        nombre.setText("");
        edad.setText("");
        raza.setText("");
        tamano.setText("");
        dueno.setText("");
        // Muestra un mensaje de que se guardo el Animal
        JOptionPane.showMessageDialog(null, "Animal guardado.");
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
 // TODO add your handling code here:
    String codigoEliminar = dueno.getText();
    boolean encontrado = false;

    if (codigoEliminar.isEmpty()) {
        // Si algun campo esta vacio, muestra un mensaje de error y no guarda los datos
        JOptionPane.showMessageDialog(null, "Por favor, ingrese el dueno.");
        return;
    }
    // iteramos sobre todos los Animal registrados hasta el indice actual
    for (int i = 0; i < animalindex; i++) {
        // Verificamos si el codigo del Animal actual coincide con el codigo a eliminar
        if (Animal[i][4].equals(codigoEliminar)) {
            // Eliminamos el Animal moviendo todos los Animal posteriores una posicion hacia adelante
            for (int j = i; j < animalindex - 1; j++) {
                Animal[j] = Animal[j + 1];
            }
            // Decrementamos el indice de Animal para reflejar que se elimino uno
            animalindex--;
            encontrado = true;
            JOptionPane.showMessageDialog(null, "Animal eliminado.");
            break; // Salimos del bucle al encontrar y eliminar el Animal
        }
    }

    // Si no se encontro ningun Animal con el codigo especificado
    if (!encontrado) {
        JOptionPane.showMessageDialog(null, "Codigo no encontrado.");
    }

    // Limpiamos el campo de texto despues de eliminar
    nombre.setText("");
    edad.setText("");
    raza.setText("");
    tamano.setText("");
    dueno.setText("");



    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        // TODO add your handling code here:
        
        // Creamos un StringBuilder para construir la cadena de texto que mostrara los datos de los Animal
        StringBuilder datoAnimal = new StringBuilder();

        // Recorremos todos los Animal registrados hasta el indice actual
        for(int i = 0; i < animalindex; i++){
            // Agregamos los datos actual al StringBuilder
            datoAnimal.append("\tAnimales\n")
            .append("Nombre: ").append(Animal[i][0]).append("\n")
            .append("Edad: ").append(Animal[i][1]).append("\n")
            .append("Raza: ").append(Animal[i][2]).append("\n")
            .append("Tamano: ").append(Animal[i][3]).append("\n")
            .append("Dueno: ").append(Animal[i][4]).append("\n\n");
        }

        // Establecemos el texto generado en el campo mostraranimales
        mostraranimales.setText(datoAnimal.toString());
    }//GEN-LAST:event_btnmostrarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
         String codigoBuscar = dueno.getText();
        boolean encontrado = false;
        // Verificar si todos los campos estan completos
        if (codigoBuscar.isEmpty()) {
            // Si algun campo esta vacio, muestra un mensaje de error y no guarda los datos
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el dueno.");
            return;
        }
        for (int i = 0; i < animalindex; i++) {
            if (Animal[i][4].equals(codigoBuscar)) {
                // Mostrar los datos del Animal encontrado en los campos de texto
                nombre.setText(Animal[i][0]);
                edad.setText(Animal[i][1]);
                raza.setText(Animal[i][2]);
                tamano.setText(Animal[i][3]);
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "Codigo no encontrado.");
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

